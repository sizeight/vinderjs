const propTypes = {
  style: PropTypes.object, // eslint-disable-line react/forbid-prop-types
  includeMarginLeft: PropTypes.bool,
  includeMarginRight: PropTypes.bool,
};

const defaultProps = {
  style: {},
  includeMarginLeft: false,
  includeMarginRight: false,
};

/*
 * Render a FontAwesomeIcon.
 * We want to limit the FontAwesome imports to this file so that we can limit our bundle size.
 */
const AppIconFontAwesome = (props) => {
  const {
    style, includeMarginLeft, includeMarginRight, ...otherProps
  } = props;

  return (
    <FontAwesomeIcon
      {...otherProps} // eslint-disable-line
      style={{
        ...style,
        marginLeft: includeMarginLeft ? '6px' : '0',
        marginRight: includeMarginRight ? '6px' : '0',
      }}
    />
  );
};

AppIconFontAwesome.propTypes = propTypes;
AppIconFontAwesome.defaultProps = defaultProps;

export default AppIconFontAwesome;
